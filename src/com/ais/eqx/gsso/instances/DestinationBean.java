package com.ais.eqx.gsso.instances;

public class DestinationBean {

	private String	nodeName		= "";
	private String	nodeCommand		= "";
	private String	nodeResultCode	= "";
	private String	nodeResultDesc	= "";

	public String getNodeName() {
		return nodeName;
	}

	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}

	public String getNodeCommand() {
		return nodeCommand;
	}

	public void setNodeCommand(String nodeCommand) {
		this.nodeCommand = nodeCommand;
	}

	public String getNodeResultCode() {
		return nodeResultCode;
	}

	public void setNodeResultCode(String nodeResultCode) {
		this.nodeResultCode = nodeResultCode;
	}

	public String getNodeResultDesc() {
		return nodeResultDesc;
	}

	public void setNodeResultDesc(String nodeResultDesc) {
		this.nodeResultDesc = nodeResultDesc;
	}

}
